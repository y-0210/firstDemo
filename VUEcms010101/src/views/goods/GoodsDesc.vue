<template>
    <div class="goodsdesc">
        <h3>{{ info.title }}</h3>
        <hr>
        <div class="content" v-html="info.content">

        </div>
    </div>

</template>

<script>
    export default {
        name: "GoodsDESC",
        data(){
            return{
                info:[]
            }
        },
        created(){
          this.getGoodsDesc()
        },
        methods:{
            getGoodsDesc(){
                this.axios.get('api/goods/getdesc/' + this.$route.params.id).then(result =>{
                    if(result.data.status === 0){
                        this.info = result.data.message[0]
                    }
                })
            }

        }
    }
</script>

